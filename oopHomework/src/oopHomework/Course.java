package oopHomework;

public class Course {
	
	int lessonId;
	String courseName;
	String courseInstructor;
	String courseDetail;
	
	public Course(int lessonId, String courseName, String courseInstructor, String courseDetail) {
		
		this.lessonId = lessonId;
		this.courseName = courseName;
		this.courseInstructor = courseInstructor;
		this.courseDetail = courseDetail;
	}
	
}
