package oopHomework;

public class Students {
	
	int studentId;
	String studentName;
	String studentSurname;
	String studentEmail;
	
	public Students(int studentId, String studentName, String studentSurname, String studentEmail) {
		
		this.studentId = studentId;
		this.studentName = studentName;
		this.studentSurname = studentSurname;
		this.studentEmail = studentEmail;
	}
}
